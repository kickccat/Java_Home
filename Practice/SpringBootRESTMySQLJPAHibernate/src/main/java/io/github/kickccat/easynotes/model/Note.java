package io.github.kickccat.easynotes.model;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import org.springframework.data.annotation.CreatedDate;
import org.springframework.data.annotation.LastModifiedDate;
import org.springframework.data.jpa.domain.support.AuditingEntityListener;

import javax.persistence.*;
import javax.validation.constraints.NotBlank;
import java.io.Serializable;
import java.util.Date;

/*
* @Entity: annotated model to mark as persistent Java class
* @Table:  the table will be mapped with this entity
* @EntityListeners(AuditingEntityListener): adds Spring Data JPA's Listener to the domain model
* @JsonIgoreProperties: jackson annotation to serialize and deserialize Java object to and from JSON
*
* @Id: defines the primary key
* @GeneratedValue(strategy=GenerationType.IDENTITY): defines the primary key generation strategy
* --> Auto Increment
* @NotBlank: validates the field is not null or empty
* @Column: defines the properties of the column mapped to the annotated field
* @Temporal: converts the date and time from Java objects to compatible database type and vice versa
* @JsonIgnoreProperties: Jackson annotation to serialize and deserialize Java objects to and from
 * JSON
* */
@Entity
@Table(name = "notes")
@EntityListeners(AuditingEntityListener.class)
@JsonIgnoreProperties(value = {"createdAt", "updatedAt"}, allowGetters = true)
public class Note implements Serializable {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @NotBlank
    private String title;

    @NotBlank
    private String content;

    @Column(nullable = false, updatable = false)
    @Temporal(TemporalType.TIMESTAMP)
    @CreatedDate
    private Date createdAt;

    @Column(nullable = false)
    @Temporal(TemporalType.TIMESTAMP)
    @LastModifiedDate
    private Date updatedAt;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public Date getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(Date createdAt) {
        this.createdAt = createdAt;
    }

    public Date getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(Date updatedAt) {
        this.updatedAt = updatedAt;
    }
}
